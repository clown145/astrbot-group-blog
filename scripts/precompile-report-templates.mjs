import { promises as fs } from "node:fs";
import path from "node:path";

import nunjucks from "nunjucks";

const projectRoot = process.cwd();
const vendorRoot = path.join(
  projectRoot,
  "src",
  "lib",
  "report-templates",
  "vendor",
);
const outputPath = path.join(
  projectRoot,
  "src",
  "lib",
  "report-templates",
  "precompiled.generated.ts",
);

function normalizeTemplateSource(source) {
  return source.replace(/\bloop\.cycle\s*\(/g, "jinja_loop_cycle(loop.index0, ");
}

function compileTemplateSource(source, fileName) {
  const result = nunjucks.precompileString(source, {
    name: fileName,
    wrapper: (templates) => templates,
  });

  if (!Array.isArray(result) || result.length !== 1) {
    throw new Error(`Unexpected precompile output for ${fileName}`);
  }

  return result[0].template;
}

async function collectTemplateGroups() {
  const entries = await fs.readdir(vendorRoot, { withFileTypes: true });
  const templateDirs = entries
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort((left, right) => left.localeCompare(right));

  const groups = [];

  for (const templateName of templateDirs) {
    const templateDir = path.join(vendorRoot, templateName);
    const files = (await fs.readdir(templateDir))
      .filter((name) => name.endsWith(".html"))
      .sort((left, right) => left.localeCompare(right));

    const compiledFiles = [];
    for (const fileName of files) {
      const filePath = path.join(templateDir, fileName);
      const source = await fs.readFile(filePath, "utf8");
      const normalizedSource = normalizeTemplateSource(source);
      const compiledCode = compileTemplateSource(normalizedSource, fileName);

      compiledFiles.push({
        fileName,
        compiledCode,
      });
    }

    groups.push({
      templateName,
      files: compiledFiles,
    });
  }

  return groups;
}

function buildModuleSource(groups) {
  const lines = [
    "// Generated by scripts/precompile-report-templates.mjs",
    "// @ts-nocheck",
    "export type PrecompiledTemplateGroup = Record<string, unknown>;",
    "export const precompiledTemplateGroups: Record<string, PrecompiledTemplateGroup> = {",
  ];

  for (const group of groups) {
    lines.push(`  ${JSON.stringify(group.templateName)}: {`);

    for (const file of group.files) {
      lines.push(`    ${JSON.stringify(file.fileName)}: (() => {`);
      for (const codeLine of file.compiledCode.trimEnd().split("\n")) {
        lines.push(`      ${codeLine}`);
      }
      lines.push("    })(),");
    }

    lines.push("  },");
  }

  lines.push("};");
  lines.push("");
  return `${lines.join("\n")}`;
}

async function main() {
  const groups = await collectTemplateGroups();
  const output = buildModuleSource(groups);
  await fs.writeFile(outputPath, output, "utf8");
  console.log(
    `Precompiled ${groups.length} template groups into ${path.relative(projectRoot, outputPath)}`,
  );
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
